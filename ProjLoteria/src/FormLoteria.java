    private void ButSortearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButSortearActionPerformed
        lot.Sortear();
        
        this.textnum1Sor.setText(String.valueOf(lot.numsort1));
        this.textnum2Sor.setText(String.valueOf(lot.numsort2));
        this.textnum3Sor.setText(String.valueOf(lot.numsort3));
        this.textnum4Sor.setText(String.valueOf(lot.numsort4));
        this.textnum5Sor.setText(String.valueOf(lot.numsort5));
        
        int numAcertos = lot.CalcAcertos();
        this.textnumAcerto.setText(String.valueOf(numAcertos));
        
        double premiacao = lot.VerificaPremio(numAcertos);
        this.textPremiacao.setText(String.valueOf(premiacao));
    }//GEN-LAST:event_ButSortearActionPerformed

