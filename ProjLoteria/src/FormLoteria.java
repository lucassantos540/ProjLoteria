        lot.num2=Double.parseDouble(this.textnum2Apo.getText());
        lot.num3=Double.parseDouble(this.textnum3Apo.getText());
        lot.num4=Double.parseDouble(this.textnum4Apo.getText());
        lot.num5=Double.parseDouble(this.textnum5Apo.getText());
        
        JOptionPane.showMessageDialog(null,"Aposta Realizada!");
        
